    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        
        if(getTotal().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes introducir un pago total", "Error en Pago", JOptionPane.ERROR_MESSAGE);
            jTextFieldTotalPago.requestFocus();
            return;
        }
        
        if(jDateChooserFEmision.getCalendar() == null) {
            JOptionPane.showMessageDialog(this, "Debes introducir una fecha de emisión", "Error en Fecha de Emision", JOptionPane.ERROR_MESSAGE);
            jDateChooserFEmision.requestFocus();
            return;
        }
        
         if(jDateChooserFPago.getCalendar() == null) {
            JOptionPane.showMessageDialog(this, "Debes introducir una fecha de pago", "Error en Fecha de Pago", JOptionPane.ERROR_MESSAGE);
            jDateChooserFEmision.requestFocus();
            return;
        }
        
        if (funcion == true) {
            if(controladorPagos.guardaPago() == true) {
               controladorPagos.rellenaTablaPagosPorId();
               jButtonGuardar.setEnabled(false);
               jButtonEditar.setEnabled(true);
            } else {
                 JOptionPane.showMessageDialog(this, "Ocurrió un error al guardar el pago", "Error al guardar", JOptionPane.ERROR_MESSAGE);
            }            
        } else {
            controladorPagos.editaPago();
            controladorPagos.rellenaTablaPagosPorId();
            jButtonGuardar.setEnabled(false);
            jButtonEditar.setEnabled(true);
        }
        
        
    }//GEN-LAST:event_jButtonGuardarActionPerformed

