    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
       
        int confirmacion = JOptionPane.showConfirmDialog(
                this,
                "Va a eliminar un Pago ¿Está seguro?",
                "Eliminar",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);
        if (confirmacion == 0) {
            controladorPagos.borraPago();
        }
        
    }//GEN-LAST:event_jButtonEliminarActionPerformed

    private void jTextFieldTotalPagoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldTotalPagoKeyTyped
     
        char caracter = evt.getKeyChar();

        // Verificar si la tecla pulsada no es un digito
        if (((caracter < '0')
                || (caracter > '9'))
                && (caracter != '\b' /*corresponde a BACK_SPACE*/)) {
            evt.consume();  // ignorar el evento de teclado
        }
        
    }//GEN-LAST:event_jTextFieldTotalPagoKeyTyped

